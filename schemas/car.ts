const car = defineType({
  name: "car",
  type: "document",
  title: "Carros",
  fields: [
    defineField({
      name: "name",
      type: "string",
      title: "Título",
      description: "ex: Renault Megane 1.5 DCI 84cv 2020",
      validation: (Rule) => Rule.required(),
    }),
    defineField({
      name: "brand",
      type: "reference",
      title: "Marca",
      to: [{ type: "brand" }],
      validation: (Rule) => Rule.required(),
    }),
    defineField({
      name: "model",
      type: "string",
      title: "Modelo",
      validation: (Rule) => Rule.required(),
    }),
    defineField({
      name: "photos",
      type: "array",
      title: "Fotos",
      of: [{ type: "image" }],
      options: {
        layout: "grid",
      },
      validation: (Rule) => Rule.required(),
    }),
    defineField({
      name: "year",
      type: "number",
      title: "Ano",
      validation: (Rule) => Rule.required().min(1900).max(2024),
    }),
    defineField({
      name: "price",
      type: "number",
      title: "Preço €",
      validation: (Rule) => Rule.required(),
    }),
    defineField({
      name: "km",
      type: "number",
      title: "Quilómetros (km)",
      validation: (Rule) => Rule.required(),
      description: "ex: 90000",
    }),
    defineField({
      title: "Caixa",
      name: "transmission",
      type: "string",
      options: {
        list: ["Manual", "Automática"],
      },
      validation: (Rule) => Rule.required(),
    }),
    defineField({
      name: "doors",
      type: "number",
      title: "Portas",
      validation: (Rule) => Rule.required().min(1).max(10),
    }),
    defineField({
      name: "seats",
      type: "number",
      title: "Lugares",
      validation: (Rule) => Rule.min(1).max(20),
    }),
    defineField({
      title: "Combustível",
      name: "fuel",
      type: "string",
      options: {
        list: fuel,
      },
      validation: (Rule) => Rule.required(),
    }),
    defineField({
      title: "Extras",
      name: "extras",
      type: "array",
      of: [{ type: "string" }],
      options: {
        list: extras.map((extra) => ({ value: extra, title: extra })),
        layout: "grid",
      },
    }),
    defineField({
      title: "Informação extra",
      name: "info",
      type: "text",
      description: "Pequeno texto com mais informação sobre o carro.",
    }),
  ],
});

export default car;
